#!/usr/bin/env python3

for f in [0x3f, 0x06, 0x5b, 0x4f, 0x66, 0x6d, 0x7d, 0x07, 0x7f, 0x6f, 0x77, 0x7c, 0x39, 0x5e, 0x79, 0x71, 0x3d, 0x76, 0x30, 0xe, 0x72, 0x38, 0x55, 0x54, 0x5c, 0x73, 0x67, 0x50, 0x6c, 0x31, 0x3e, 0x1c, 0x49, 0x52, 0x6a, 0x1b]:
    if (f & 1): print('A', end='')
    if (f & 2): print('B', end='')
    if (f & 4): print('C', end='')
    if (f & 8): print('D', end='')
    if (f & 16): print('E', end='')
    if (f & 32): print('F', end='')
    if (f & 64): print('G', end='')
    if (f & 128): print('H', end='')
    print()

